CREATE TABLE IF NOT EXISTS chore_records (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  category TEXT NOT NULL,
  task TEXT NOT NULL,
  note TEXT,
  score INTEGER,
  multiplier INTEGER DEFAULT 1,
  assignee TEXT
);

CREATE TABLE IF NOT EXISTS diary_records (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  content TEXT NOT NULL,
  date DATE NOT NULL DEFAULT CURRENT_DATE
);

CREATE TABLE IF NOT EXISTS plant_info (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  variety TEXT,
  planting_date DATE,
  location TEXT,
  status TEXT DEFAULT 'growing',
  price INTEGER,
  quantity INTEGER,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS plant_records (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  plant_id BIGINT REFERENCES plant_info(id) ON DELETE CASCADE,
  recorded_at TIMESTAMPTZ DEFAULT NOW(),
  record_type TEXT NOT NULL,
  note TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- RLS Settings
ALTER TABLE plant_info ENABLE ROW LEVEL SECURITY;
ALTER TABLE plant_records ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Allow all for plant_info" ON plant_info FOR ALL USING (true) WITH CHECK (true);
CREATE POLICY "Allow all for plant_records" ON plant_records FOR ALL USING (true) WITH CHECK (true);
